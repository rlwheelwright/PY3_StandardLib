# Minimum and Maximum

playerOneScore = 10
playerTwoScore = 4
print(min(playerOneScore, playerTwoScore)) # Prints 4 from player 2
print(min(0, 12, -19, 3, 17, 4))
print(min("Kathryn", "Katie")) # Prints Kathryn because h < i
print(min("Angela", "Agnus", "Bob", "Angel"))

print(max(playerOneScore, playerTwoScore)) # Prints 4 from player 2
playerThreeScore = 14
print(max(playerThreeScore, playerTwoScore, playerOneScore))
print(max("Kathryn", "Katie")) # Prints Kathryn because i > h
