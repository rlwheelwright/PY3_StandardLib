# Python Rounding, Absolute Value, and Exponents

# round()
myGPA = 3.6
print(round(myGPA))
amountOfSalt = 1.4 # pinches of salt
print(round(amountOfSalt))

apple = -1.2
print(round(apple))
google = -1.6
print(round(google))

# abs()
distanceAway = -13
print(abs(distanceAway)) # Returns 13
lengthOfRootInGround = -2.5
print(abs(lengthOfRootInGround))

# pow()
pow(3, 3) # = 3 * 3 * 3 = 27
chanceOfTails = 0.5
inARowTails = 3
print(pow(chanceOfTails, inARowTails))

chanceOfOne = 0.167
inARowOne = 2
print(pow(chanceOfOne, inARowOne))
