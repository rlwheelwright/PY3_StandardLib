# Math Module Part 2
import math

# Factorial & Square Root
print(math.factorial(3)) # = 3 * 2 * 1 = 6
print(math.sqrt(16)) # Returns float

# Greatest Common Denominator GCD
math.gcd(8, 52) # order (8, 52) or (52, 8) doesn't matter

# Degrees and Radians
print(math.radians(360)) # Radians in a circle; ~ 6.28
print(math.pi * 2) # Same result as above
print(math.degrees(math.pi * 2)) # 360 (does both, above)
